public class operator_example {
    public static void main(String[] args){
        double a = 10;
        double b = 20;
        double p,s,m,d,tmp = 0;
        p = a+b;
        s = a-b;
        m = a*b;
        d = a/b;
        tmp = Math.pow(a , 3);
        System.out.println(a+" "+b+"\n"+p+"\n"+s+"\n"+m+"\n"+d+"\n"+tmp);
    }
}
