public class printf_example {
    public static void main(String[] args){
        int a = 3;
        double b= 3.14;
        String str = "안녕";
        char c = 'a';
        System.out.printf("a = %d%n", a);
        System.out.printf("b = %f%n", b);
        System.out.printf("나는 %s%n", str);
        System.out.printf("a = %c", c);




    }
}
